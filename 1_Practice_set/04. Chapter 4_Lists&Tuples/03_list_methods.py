l1 = [1,8,7,2,21,15]
print(l1)

# Sorts the list
l1.sort()
print(l1)

# Reverses the list
l1.reverse()
print(l1)

# Append adds the value at the end of the list 
l1.append(9)
print(l1)

# Insert adds the value at the given position
l1.insert(1,544)
print(l1)

# Pop removes the value available at the given index
l1.pop(1)
print(l1)

# Remove a value from the list
l1.remove(9)
print(l1)